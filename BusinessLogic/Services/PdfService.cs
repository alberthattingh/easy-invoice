using System.Collections.Generic;
using Api2Pdf;
using BusinessLogic.Helpers;

namespace BusinessLogic.Services
{
    public class PdfService : IPdfService
    {
        private readonly Api2Pdf.Api2Pdf PdfClient;

        public PdfService(AppSettings appSettings)
        {
            PdfClient = new Api2Pdf.Api2Pdf(appSettings.ApiToPdfToken);
        }

        public string GeneratePdf(string html)
        {
            // TODO: Add to custom storage and make bucket public
            // Also make logos bucket public, and send URL to logo in template
            var options = new Dictionary<string, string>();
            options.Add("orientation", "portrait");
            options.Add("pageSize", "Letter");
            options.Add("footerCenter", "Generated by EasyInvoice");

            var request = new WkhtmlHtmlToPdfRequest
            {
                Html = html,
                Inline = true,
                Options = options,
            };

            var apiResponse = PdfClient.Wkhtml.HtmlToPdf(request);
            return apiResponse.FileUrl;
        }
    }
}